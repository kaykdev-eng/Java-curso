public class Expressao {
    static void main(String[] args){
        int x;
        double y;
        x = 5;
        y = x + 5;
        System.out.println(y);

        double b, B, h, area;
        b = 6.0;
        B = 8.0;
        h = 5;
        area = (b + B) / 2 * h;
        System.out.println(area);

        int a, z;
        double resultado;
        a = 5;
        z = 2;
        resultado = (double) a / z;
        System.out.println(resultado);

    }
}
